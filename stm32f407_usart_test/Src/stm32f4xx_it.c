extern uint8_t u1_rx_buffer[UART_RX_SIZE], u2_rx_buffer[UART_RX_SIZE];
/**
  * @brief This function handles USART1 global interrupt.
  */
 uint8_t usart1_int_cnt, usart1_idle_cnt;
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
    usart1_int_cnt++;
    if (USART1->SR & 0x0010) {
        usart1_idle_cnt++;
        __HAL_DMA_DISABLE(&hdma_usart1_rx);
        /* This part is important */
        /* Clear IDLE flag by reading status register first */
        /* And follow by reading data register */
        volatile uint32_t tmp; /* Must be volatile to prevent optimizations */
        tmp = USART1->SR;      /* Read status register */
        tmp = USART1->DR;      /* Read data register */

        DMA2_Stream2->M0AR = (uint32_t)u1_rx_buffer; /* Set memory address for DMA again */
        DMA2_Stream2->NDTR = UART_RX_SIZE;               /* Set number of bytes to receive */
        __HAL_DMA_ENABLE(&hdma_usart1_rx);
    }
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

/**
  * @brief This function handles USART2 global interrupt.
  */
uint8_t usart2_idle_cnt;
void USART2_IRQHandler(void) {
    /* USER CODE BEGIN USART3_IRQn 0 */
    if (USART2->SR & 0x0010) {
        usart2_idle_cnt++;
        __HAL_DMA_DISABLE(&hdma_usart2_rx);
        /* This part is important */
        /* Clear IDLE flag by reading status register first */
        /* And follow by reading data register */
        volatile uint32_t tmp; /* Must be volatile to prevent optimizations */
        tmp = USART2->SR;       /* Read status register */
        tmp = USART2->DR;       /* Read data register */

        DMA1_Stream5->M0AR = (uint32_t)u2_rx_buffer; /* Set memory address for DMA again */
        DMA1_Stream5->NDTR = UART_RX_SIZE;               /* Set number of bytes to receive */
        __HAL_DMA_ENABLE(&hdma_usart2_rx);
    }
    /* USER CODE BEGIN USART2_IRQn 0 */
    /* USER CODE END USART2_IRQn 0 */
    /*
            Allow HAL_UART_IRQHandler will make the DMA_buffer stop working
            Here's the cause of problem
    */
    /* USER CODE END USART3_IRQn 0 */
    HAL_UART_IRQHandler(&huart2);
    /* USER CODE BEGIN USART3_IRQn 1 */

    /* USER CODE END USART3_IRQn 1 */
}

